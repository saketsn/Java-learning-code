public class SwitchChallange {
    public static void main(String[] args) {

            char charVariable = 'x';

        switch (charVariable) {
            case 'A' -> System.out.println(" The word is Able ");
            case 'B' -> System.out.println(" The word is Baker ");
            case 'C' -> System.out.println(" The word is Charlie ");
            case 'D' -> System.out.println(" The word is Dog ");
            case 'E' -> System.out.println(" The word is Easy ");
            default -> System.out.println(" The word is not found ");
        }


    }
}
